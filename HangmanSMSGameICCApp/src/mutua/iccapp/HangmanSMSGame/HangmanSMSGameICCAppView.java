    private String[] uniqueUserColors = {"#404040", "#804040", "#709050", "#907050", "#408040", "#509070", "#404080", "#808040", "#408080", "#804080", "#705090", "#905070", "#507090"};
    private Hashtable<String, Integer> uniqueUsers = new Hashtable<String, Integer>();
    private String txt = "";
    private void btn_simulateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simulateActionPerformed

        try {

            // desativa controles
            btn_simulate.setEnabled(false);
            txa_message.setEnabled(false);

            String phone = txb_msisdn.getText();
            String carrier = (String)cmb_carrier.getSelectedItem();
            String inputText = txa_message.getText();
            
            // manage user uniqueness and display color
            int uniqueUserPosition;
            if (uniqueUsers.containsKey(phone)) {
            	uniqueUserPosition = uniqueUsers.get(phone);
            } else {
            	uniqueUserPosition = uniqueUsers.size();
            	uniqueUsers.put(phone, uniqueUserPosition);
            }
            String userColor = uniqueUserColors[uniqueUserPosition % uniqueUserColors.length];


            String dateStr;
            dateStr = DateTimeHelper.getSimpleDateStringFromMillis(System.currentTimeMillis());
            txt += "<br/><font color="+userColor+">&gt;&gt;&gt; <i>("+dateStr+") " + phone + "</i></font>: <b>" + inputText + "</b>";
            
            String[][] MTs = smsFrontend.process(phone, inputText, carrier);
            
			for (int i=0; i<MTs.length; i++) {
				String outgoingBillingType = MTs[i][0];
				String outgoingPhone       = MTs[i][1];
				String outgoingText        = MTs[i][2];

				// number of chars presentation
				String chars = outgoingText.length() + " chars";
				if (outgoingText.length() > 138) {
					chars = "<font color='#FF0000'>" + chars + "</font>";
				}
				
				dateStr = DateTimeHelper.getSimpleDateStringFromMillis(System.currentTimeMillis());
				txt += "<br/>"+"<font color="+userColor+">&lt;&lt;&lt; <i>("+dateStr+") " + outgoingPhone + "</i></font>: '" + outgoingText + "' <i>("+chars+", <b>"+outgoingBillingType+"</b>)</i>";
			}

            txp_history.setText("<html><head></head><body><p style='margin-top: 0'>"+txt+"</p></body></html>");

            txa_message.setText("");
        } finally {
            btn_simulate.setEnabled(true);
            txa_message.setEnabled(true);
            txa_message.requestFocus();
        }
}//GEN-LAST:event_btn_simulateActionPerformed

